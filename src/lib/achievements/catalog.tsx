function entry(
  id: string,
  section: WinsSection,
  tier: MilestoneTier,
  title: string,
  subtitle: string,
  lockedHint: string,
  gridLockedLabel: string,
  Icon: LucideIcon,
  check: (c: AchievementCtx) => boolean,
): AchievementCatalogEntry {
  return { id, section, tier, title, subtitle, lockedHint, gridLockedLabel, Icon, check };
}

function clearedDebts(c: AchievementCtx) {
  return c.debts.filter((d) => d.balance <= 0.01);
}

function smallestCleared(c: AchievementCtx): boolean {
  if (c.debtsCleared < 1 || c.debts.length === 0) return false;
  const minAll = Math.min(...c.debts.map((d) => d.initialBalance));
  return clearedDebts(c).some((d) => Math.abs(d.initialBalance - minAll) < 0.01);
}

function largestCleared(c: AchievementCtx): boolean {
  if (c.debtsCleared < 1 || c.debts.length === 0) return false;
  const maxAll = Math.max(...c.debts.map((d) => d.initialBalance));
  return clearedDebts(c).some((d) => Math.abs(d.initialBalance - maxAll) < 0.01);
}

function highestAprCleared(c: AchievementCtx): boolean {
  const cl = clearedDebts(c);
  if (cl.length === 0) return false;
  const maxRate = Math.max(...cl.map((d) => d.interestRate));
  return cl.some((d) => d.interestRate >= maxRate - 0.0001);
}

