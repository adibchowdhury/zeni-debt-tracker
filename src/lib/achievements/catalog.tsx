const pctIcon = TrendingUp;
const moneyIcon = Banknote;

function pctBadge(
  id: string,
  minPct: number,
  title: string,
  section: WinsSection,
  tier: MilestoneTier,
): AchievementCatalogEntry {
  return entry(
    id,
    section,
    tier,
    title,
    `Pay down ${minPct}% of your starting balances.`,
    `Reach ${minPct}% paid off across your debts.`,
    "Keep stacking payments",
    pctIcon,
    (c) => c.totalInitial > 0 && c.pctPaid >= minPct - 0.0001,
  );
}

function moneyBadge(
  id: string,
  min: number,
  title: string,
  tier: MilestoneTier,
): AchievementCatalogEntry {
  return entry(
    id,
    "money-milestones",
    tier,
    title,
    "Lifetime payments logged in Zeni.",
    `Log ${formatUsdTitle(min)} in total payments.`,
    "Keep stacking payments",
    moneyIcon,
    (c) => c.totalPaid >= min - 0.009,
  );
}

function formatUsdTitle(n: number): string {
  if (n >= 1000) return `$${(n / 1000).toLocaleString(undefined, { maximumFractionDigits: n % 1000 === 0 ? 0 : 1 })}k`;
  return `$${n}`;
}

